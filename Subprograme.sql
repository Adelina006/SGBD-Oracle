--Funcția  verifica_salariul returnează TRUE/FALSE daca salariatul are salariul mai mare/mai mic sau egal cu salariul mediu  si NULL daca salariatul nu exista
CREATE OR REPLACE FUNCTION VERIFICA_SAL ( P_ID_ANGAJAT IN ANGAJATI.ID_ANGAJAT%TYPE, P_MEDIU IN NUMBER) RETURN BOOLEAN
IS
V_SAL ANGAJATI.SALARIUL%TYPE;
BEGIN
SELECT SALARIUL INTO V_SAL FROM ANGAJATI WHERE ID_ANGAJAT = P_ID_ANGAJAT;
IF V_SAL < P_MEDIU THEN RETURN FALSE;
ELSE RETURN TRUE;
END IF;
EXCEPTION
WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NU EXISTA ANGAAJTUL');
WHEN OTHERS THEN DBMS_OUTPUT.PUT_LINE('A APARUT O EROARE');
END;
/

DECLARE 
MEDIU NUMBER;
REZ BOOLEAN;
BEGIN
SAL_MEDIU(MEDIU);
DBMS_OUTPUT.PUT_LINE(ROUND(MEDIU, 3));
REZ := VERIFICA_SAL(176, MEDIU);
IF REZ = TRUE THEN DBMS_OUTPUT.PUT_LINE('ARE SAL MAI MARE');
ELSE
DBMS_OUTPUT.PUT_LINE('ARE SAL MAI MIC');
END IF;
END;
/

DESCRIBE VERIFICA_SAL;   

