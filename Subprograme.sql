MEDIU NUMBER;
BEGIN
SAL_MEDIU(MEDIU);
DBMS_OUTPUT.PUT_LINE (ROUND(MEDIU, 2));
MODIFICARE(105, 10);
SAL_MEDIU(MEDIU);
DBMS_OUTPUT.PUT_LINE (ROUND(MEDIU, 2));
END;
/

