--Procedura primeşte ca parametru de tip IN id_ul unui angajat şi returnează prin parametrii de tip OUT numele şi salariul acestuia:
CREATE OR REPLACE PROCEDURE ANGAJAT (P_ID_ANGAJAT IN ANGAJATI.ID_ANGAJAT%TYPE, P_NUME OUT ANGAJATI.NUME%TYPE,
P_SALARIUL OUT ANGAJATI.SALARIUL%TYPE)
IS
BEGIN
SELECT NUME, SALARIUL INTO P_NUME, P_SALARIUL FROM ANGAJATI WHERE ID_ANGAJAT = P_ID_ANGAJAT;
DBMS_OUTPUT.PUT_LINE('ANGAJATUL '|| P_NUME||' ARE SAALRIUL '|| P_SALARIUL);
END;
/

DECLARE 
NUME ANGAJATI.NUME%TYPE;
SALARIUL ANGAJATI.SALARIUL%TYPE;
BEGIN
ANGAJAT(176, NUME, SALARIUL);
END;
/

--Procedura calculează salariul mediu şi îl returnează printr-un parametru de tip OUT:
CREATE OR REPLACE PROCEDURE SAL_MEDIU (P_MEDIU OUT NUMBER)
IS
BEGIN
SELECT AVG(SALARIUL) INTO P_MEDIU FROM ANGAJATI;
END;
