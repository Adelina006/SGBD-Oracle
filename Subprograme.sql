--Procedura modifică salariul unui angajat doar în cazul în care este mai mic decât media prin apelarea procedurii create mai sus, MODIFICA_SALARIUL_PROCENT. Procedura primeşte id-ul angajatului ca parametru de intrare şi salariul mediu actual şi prin returnează prin parametrul de tip IN OUT salariul mediu modificat prin apelul procedurii SAL_MEDIU.
CREATE OR REPLACE PROCEDURE MODIFICARE (P_ID_ANGAJAT IN ANGAJATI.ID_ANGAJAT%TYPE, P_PROCENT IN NUMBER)
IS
V_MEDIU NUMBER;
V_SALARIUL ANGAJATI.SALARIUL%TYPE;
V_NUME ANGAJATI.NUME%TYPE;
BEGIN
ANGAJAT(P_ID_ANGAJAT, V_NUME, V_SALARIUL);
SAL_MEDIU (V_MEDIU);
SELECT SALARIUL INTO V_SALARIUL FROM ANGAJATI WHERE ID_ANGAJAT = P_ID_ANGAJAT;
IF V_SALARIUL < V_MEDIU THEN MODIFICA_SAL_PROCENT(P_ID_ANGAJAT, P_PROCENT);
END IF;
ANGAJAT(P_ID_ANGAJAT, V_NUME, V_SALARIUL);
END;
/
