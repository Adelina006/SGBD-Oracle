MODIFICA_SAL_PROCENT(P_ID_ANGAJAT IN ANGAJATI.ID_ANGAJAT%TYPE, PROCENT IN NUMBER)
IS
V_SALARIUL ANGAJATI.SALARIUL%TYPE;
